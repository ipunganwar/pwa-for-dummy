// *** CACHE Then Network And Support Offline Fallback
self.addEventListener('fetch', function(event) {
  const url = 'https://food-ninja-pwa-23296.firebaseio.com/posts'

  if (event.request.url.indexOf(url) > -1) {
    event.respondWith(
      fetch(event.request).then(res => {
        let cloneRes = res.clone()
        cloneRes.json().then(data => {
          for (let key in data) {
            dbPromise.then (db => {
              console.log('>>>', db)
              let tx = db.transaction('posts', 'readwrite')
              let store = tx.objectStore('posts')
              store.put(data[key])

              console.log('>>> tx', tx)
              return tx.complete
            })
          }
        })

        return res
      })
    );
  } else if (isInArray(event.request.url, STATIC_FILE)) {
    event.respondWith(
      caches.match(event.request)
    );
  } else {
    // Fallback with offline strategy

    event.respondWith(
      caches.match(event.request)
        .then(function(response) {
          if (response) {
            return response;
          } else {
            return fetch(event.request)
              .then(function(res) {
                return res;
              })
              .catch(function(err) {
                return caches.open(CACHE_STATIC_NAME)
                .then(cache => {
                  return cache.match('/offline.html')
                })
              });
          }
        })
    );
  }
  
});